-- Adhoc analysis for Alexandria Region: 

-- gathering all alexandria region sales data to analyze the performance of this region and identify any specific trends or patterns in sales for this area.
   
-- we need to convert the zone_name column to varchar(max) to avoid any conversion error due to the presence of text data in the zone_name. 
ALTER TABLE dbo.sales_copy 
ALTER COLUMN zone_name varchar(max);

-- Analyze Alexandria sales with running totals per zone
SELECT 
    CAST(distributer_name AS varchar(max)) AS distributer_name,
    CAST(zone_name AS varchar(max)) AS zone_name,
    revenue,
    quantity_sold,
    SUM(revenue) OVER (PARTITION BY CAST(zone_name AS varchar(max))) AS zone_total_revenue,
    SUM(quantity_sold) OVER (PARTITION BY CAST(zone_name AS varchar(max))) AS zone_total_quantity
FROM dbo.sales_copy
WHERE zone_name LIKE '%Alex%' 
   OR zone_name LIKE '%sem%' 
   OR zone_name LIKE '%Mohar%'
ORDER BY zone_total_revenue desc
 -- This query will help us analyze the sales performance of the Alexandria region by summing up the total revenue and quantity sold for that specific region, allowing us to identify any trends or patterns in sales for that area.






