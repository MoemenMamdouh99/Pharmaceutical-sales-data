-- checking for any duplicate records in sales_copy table 
with dublicates as (
select sales_date, distributer_name, product_name, area_manager, medical_rep, district_manager, line_name, revenue, quantity_sold, zone_name, price,
ROW_NUMBER() over(partition by sales_date, CAST(distributer_name AS NVARCHAR(MAX)),
                CAST(product_name AS NVARCHAR(MAX)),
                CAST(area_manager AS NVARCHAR(MAX)),
                CAST(medical_rep AS NVARCHAR(MAX)),
                CAST(district_manager AS NVARCHAR(MAX)),
                CAST(line_name AS NVARCHAR(MAX)),
                revenue,
                quantity_sold,
                CAST(zone_name AS NVARCHAR(MAX)),
                price order by (revenue)) as rn
from dbo.sales_copy 
) 

select * 
from dublicates 
where rn > 1                    -- No dublicate records found in sales_copy table. 


